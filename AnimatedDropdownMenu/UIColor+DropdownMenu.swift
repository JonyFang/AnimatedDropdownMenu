import UIKit

extension UIColor {
    
    class func menuBackgroundColor() -> UIColor {
        return UIColor(red: 57/255.0, green: 67/255.0, blue: 79/255.0, alpha: 1.0)
    }
    
    class func menuLightGrayColor() -> UIColor {
        return UIColor(red: 95/255.0, green: 105/255.0, blue: 117/255.0, alpha: 1.0)
    }
    
    class func menuDarkGrayColor() -> UIColor {
        return UIColor(red: 49/255.0, green: 59/255.0, blue: 71/255.0, alpha: 1.0)
    }
    
    class func menuLightTextColor() -> UIColor {
        return UIColor(red: 245/255.0, green: 245/255.0, blue: 245/255.0, alpha: 1.0)
    }
    
    class func menuLightRedColor() -> UIColor {
        return UIColor(red: 216/255.0, green: 85/255.0, blue: 96/255.0, alpha: 1.0)
    }
    
    class func menuDarkRedColor() -> UIColor {
        return UIColor(red: 160/255.0, green: 61/255.0, blue: 73/255.0, alpha: 1.0)
    }
}
